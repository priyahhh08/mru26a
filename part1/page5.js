//Converting data types
let a = 10
console.log(a) // 10
let b = String(a)
console.log(b) // "10"
let c = Number(b)
console.log(c) // 10
let d = Boolean(a)
console.log(d) // true
let e = Boolean(0)
console.log(e) // false


let str = string(a)
console.log(typeof str) // string
let num = Number(str)
console.log(typeof num) // number

console.log("5" + 10) // "510"
console.log("5" - 10) // -5
console.log("5" * 10) // 50
console.log("5" / 10) // 0.5

