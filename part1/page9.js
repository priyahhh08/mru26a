//for loops and nested loop
for (let i = 0; i < 5; i++) {//initialization, condition, increment
    console.log(i)
}

//nested loop
for (let i = 1; i <= 3; i++) { //outer loop
    for (let j = 1; j <= 3; j++) { //inner loop
        console.log(i)
    }
}

for (let i = 1; i <= 3; i++) {//outer loop
    for (let j = 1; j <= 3; j++) {//inner loop
        console.log(j)
    }
}